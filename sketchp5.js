var bubbles = [];


function setup() {
  createCanvas(window.innerWidth, window.innerHeight);
  for (var i = 0; i < 4; i++) {
    bubbles[i] = new Bubble(); 
  }
}

function draw() {
  background(255);
  for (var i = 0; i < bubbles.length; i++) {
    bubbles[i].move();
    bubbles[i].display();
  }
}

function Bubble(){
  this.x = random(0, width);
  this.y = random(0, height);
  
  this.display = function() {
//    stroke(50,180,250,100);
    // strokeWeight(1);
    noStroke();
    fill(50,random(180),250);
    ellipse(this.x, this.y, 24, 24);
    ellipse(this.x+10,this.y+10,24,24);
    ellipse(this.x+30,this.y+10,24,24);
    ellipse(this.x+30,this.y-10,24,24);
    ellipse(this.x+20,this.y-10,24,24);
    ellipse(this.x+40,this.y,24,24);
  }
  
  this.move = function() {
    this.x = this.x += 1 ;
    this.y = this.y + random(-1, 1);
    
    if(this.x >= width){
    this.x = 0;
    }
  }
}


function mouseClicked(){
  bubbles.push(new Bubble());
}

function keyPressed(){
  bubbles.splice(Bubble.length-1,1);
}